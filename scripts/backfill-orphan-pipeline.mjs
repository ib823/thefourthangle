#!/usr/bin/env node
/**
 * Backfill engine pipeline artifacts for an edited orphan issue.
 *
 * Given an issue id, generates:
 *   engine/briefs/{slug}.md                    — research brief stub from current cards
 *   engine/output/{slug}-stage1.json           — Stage 1 JSON from current cards
 *   engine/prompts-generated/{slug}-stage{2,3,4,5}-browser.txt
 *                                              — ready-to-paste prompts using the
 *                                                strengthened preamble templates
 *
 * The slug is derived from the headline (kebab-case, max 50 chars).
 * Existing files for that slug are NOT overwritten unless --force is passed.
 *
 * Usage:
 *   node scripts/backfill-orphan-pipeline.mjs <issue-id> [--force]
 *   node scripts/backfill-orphan-pipeline.mjs --batch <id1,id2,id3,...>
 */

import { readFileSync, writeFileSync, existsSync, mkdirSync, readdirSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const args = process.argv.slice(2);
const force = args.includes('--force');
const batchIdx = args.indexOf('--batch');
const ids = batchIdx >= 0 ? args[batchIdx + 1].split(',') : args.filter(a => /^\d{4}$/.test(a));

if (ids.length === 0) {
  console.error('Usage: node scripts/backfill-orphan-pipeline.mjs <id> [--force]');
  console.error('       node scripts/backfill-orphan-pipeline.mjs --batch 1043,1190,1751');
  process.exit(1);
}

// Load issues
const tsContent = readFileSync(join(root, 'src', 'data', 'issues.ts'), 'utf8');
const m = tsContent.match(/export const ISSUES:\s*Issue\[\]\s*=\s*(\[[\s\S]*\]);?\s*$/m);
const issues = eval('(' + m[1].replace(/;\s*$/, '') + ')');

// Existing slug mapping (so we reuse slug if one already exists)
const idToSlug = {};
const engineOutDir = join(root, 'engine', 'output');
if (existsSync(engineOutDir)) {
  for (const f of readdirSync(engineOutDir).filter(f => f.endsWith('-reader.json'))) {
    const slug = f.replace(/-reader\.json$/, '');
    try {
      const j = JSON.parse(readFileSync(join(engineOutDir, f), 'utf8'));
      if (j.id) idToSlug[j.id] = slug;
    } catch (_) {}
  }
}

function slugify(headline) {
  return headline
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, '')
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '')
    .slice(0, 50)
    .replace(/-$/, '');
}

function buildBrief(issue, slug) {
  const today = new Date().toISOString().slice(0, 10);
  const lenses = [...new Set((issue.cards || []).filter(c => c.lens).map(c => c.lens))];

  const lines = [];
  lines.push(`# Research Brief — Issue ${issue.id}`);
  lines.push('');
  lines.push(`**Slug:** \`${slug}\``);
  lines.push(`**Headline:** ${issue.headline}`);
  lines.push(`**Source date:** ${issue.sourceDate || today}`);
  lines.push(`**Edition:** ${issue.edition} (${issue.status || 'new'})`);
  lines.push('');
  lines.push('> **Backfill notice.** This brief was generated by `scripts/backfill-orphan-pipeline.mjs` from the currently-published cards on ' + today + '. It is a STUB — it captures the claims and source signals already in the cards but does NOT replace a primary-source research pass. Before running this issue through Stages 2-5 of the pipeline, expand this brief with verified primary sources for every numerical claim, named actor, and law citation.');
  lines.push('');
  lines.push('## ISSUE');
  lines.push('');
  lines.push(issue.context || '(No context recorded)');
  lines.push('');
  lines.push('## CURRENT CARDS (post-audit edition)');
  lines.push('');
  for (let ci = 0; ci < (issue.cards || []).length; ci++) {
    const c = issue.cards[ci];
    lines.push(`### Card ${ci} — ${c.t}${c.lens ? ' [' + c.lens + ']' : ''}`);
    lines.push('');
    if (c.big) lines.push(`**Big:** ${c.big}`);
    if (c.sub) lines.push(`**Sub:** ${c.sub}`);
    lines.push('');
  }

  lines.push('## ACTORS');
  lines.push('');
  lines.push('_To be filled in during research expansion. Identify every named institution, person, and community referenced in the cards above. For each, note their role and the position attributed to them._');
  lines.push('');

  lines.push('## RELEVANT LAW');
  lines.push('');
  lines.push('_To be filled in. List every legislation, constitutional article, regulation, or court case referenced in the cards. For each, cite the exact section number and verify with primary sources._');
  lines.push('');

  lines.push('## KEY STATISTICS');
  lines.push('');
  lines.push('_To be filled in. List every numerical claim in the cards above. For each, the brief MUST include: exact source URL, document title, page or section number, and publication date. "BNM 2024" is not a citation — "BNM Annual Report 2024, Table 3.2, p. 47, https://..." is._');
  lines.push('');

  lines.push('## 12-DIMENSION RISK ASSESSMENT');
  lines.push('');
  lines.push('_To be filled in. For each dimension below, assess risk level (LOW/MEDIUM/HIGH/CRITICAL) and justify._');
  lines.push('');
  lines.push('- Sentiment: ');
  lines.push('- Political: ');
  lines.push('- Ethnic: ');
  lines.push('- Religious: ');
  lines.push('- Narrative: ');
  lines.push('- Completeness: ');
  lines.push('- Temporal: ');
  lines.push('- Confidence: ');
  lines.push('- Sources: ');
  lines.push('- Geographic: ');
  lines.push('- Economic: ');
  lines.push('- Gender: ');
  lines.push('');

  lines.push('## RECOMMENDED LENSES');
  lines.push('');
  if (lenses.length > 0) {
    lines.push(lenses.map(l => '- ' + l).join('\n'));
  } else {
    lines.push('_No lenses currently assigned._');
  }
  lines.push('');

  lines.push('## SOURCES');
  lines.push('');
  lines.push('_To be filled in. 15-25 citations, of which at least 8 must be PRIMARY (government docs, gazettes, court rulings, regulator publications, peer-reviewed papers). Sources must span the political spectrum (government-aligned, opposition-aligned, independent, international body, community voice, business). For every quantitative claim above: exact URL, document title, page/section, publication date._');
  lines.push('');

  lines.push('## CONTRADICTIONS');
  lines.push('');
  lines.push('_To be filled in. Any time two reputable sources gave different numbers, dates, or characterisations of the same fact, list both with citations and identify which is most authoritative and why._');
  lines.push('');

  lines.push('## SOURCE SPECTRUM CHECK');
  lines.push('');
  lines.push('_Note explicitly which side of the political spectrum each major source comes from. Flag if all sources are from one side._');
  lines.push('');

  lines.push('## BACKFILL CONTEXT');
  lines.push('');
  lines.push(`This issue was added to the unauditable backlog before the current pipeline existed. During the 2026-04-08 audit pass it was reviewed against the new Accuracy Standard. The current published cards already incorporate corrections from that pass. This brief preserves the post-audit state as the baseline for any future Stages 2-5 review.`);
  lines.push('');

  return lines.join('\n');
}

function buildStage1Json(issue) {
  const cards = (issue.cards || []).map(c => {
    const out = { t: c.t };
    if (c.lens) out.lens = c.lens;
    if (c.big) {
      if (c.t === 'hook' || c.t === 'reframe' || c.t === 'view' || c.t === 'analogy') {
        out.text = c.big;
      } else {
        out.h = c.big;
      }
    }
    if (c.sub) {
      if (c.t === 'hook' || c.t === 'analogy') {
        out.sub = c.sub;
      } else if (c.t === 'reframe' || c.t === 'view') {
        // these usually have empty sub; preserve as text2 if non-empty
        if (c.sub) out.sub = c.sub;
      } else {
        out.s = c.sub;
      }
    }
    out.sources = ['BACKFILL: traced from currently-published cards. Pre-pipeline issue; primary sources to be added in research brief expansion.'];
    return out;
  });
  const lenses = [...new Set((issue.cards || []).filter(c => c.lens).map(c => c.lens))];

  return {
    cards,
    sources: 'BACKFILL — see brief for source list (to be expanded)',
    confidence: 'Backfilled from current published cards. Treat as Stage 1 baseline only — primary-source verification to be done via stage2-5 pass.',
    lenses_used: lenses,
    lenses_applicable_but_unused: [],
    needs_verification: ['ALL specific numbers, dates, named actors and law citations in the cards above need primary-source verification before re-publication.'],
    source_notes: 'Backfill artifact. The currently-published cards reflect the 2026-04-08 audit-driven corrections; this Stage 1 JSON preserves that state as the baseline.',
    issue_id: issue.id,
    issue_headline: issue.headline,
  };
}

function buildBrowserPrompt(stageN, verb, label, preamble, stage1Json) {
  const separator = `\n\n=== THE ANALYSIS TO ${verb} ===\n\n`;
  return preamble.trimEnd() + separator + JSON.stringify(stage1Json, null, 2) + '\n';
}

const STAGES = [
  { n: 2, verb: 'AUDIT', label: 'Bias Audit (Gemini)' },
  { n: 3, verb: 'FACT-CHECK', label: 'Fact Verification (ChatGPT)' },
  { n: 4, verb: 'REVIEW', label: 'Alternative Framing (DeepSeek/ChatGPT)' },
  { n: 5, verb: 'STRESS-TEST', label: 'Contrarian Stress-Test (Grok)' },
];

const briefDir = join(root, 'engine', 'briefs');
const promptDir = join(root, 'engine', 'prompts-generated');
mkdirSync(briefDir, { recursive: true });
mkdirSync(engineOutDir, { recursive: true });
mkdirSync(promptDir, { recursive: true });

const summary = [];

for (const id of ids) {
  const issue = issues.find(i => i.id === id);
  if (!issue) {
    console.error(`SKIP ${id}: not found in issues.ts`);
    continue;
  }
  const slug = idToSlug[id] || slugify(issue.headline) || `issue-${id}`;

  const briefPath = join(briefDir, `${slug}.md`);
  const stage1Path = join(engineOutDir, `${slug}-stage1.json`);

  // Brief
  if (!existsSync(briefPath) || force) {
    writeFileSync(briefPath, buildBrief(issue, slug));
  }

  // Stage 1
  const stage1Json = buildStage1Json(issue);
  if (!existsSync(stage1Path) || force) {
    writeFileSync(stage1Path, JSON.stringify(stage1Json, null, 2));
  }

  // Browser prompts (always regenerate since they depend on Stage 1)
  for (const { n, verb, label } of STAGES) {
    const preamblePath = join(root, 'engine', 'templates', `stage${n}-preamble.txt`);
    const preamble = readFileSync(preamblePath, 'utf-8');
    const promptPath = join(promptDir, `${slug}-stage${n}-browser.txt`);
    if (!existsSync(promptPath) || force) {
      writeFileSync(promptPath, buildBrowserPrompt(n, verb, label, preamble, stage1Json));
    }
  }

  summary.push({ id, slug });
}

console.log('');
console.log(`  Backfilled pipeline artifacts for ${summary.length} issues:`);
console.log('  ' + '─'.repeat(60));
for (const s of summary) {
  console.log(`  ${s.id}  →  ${s.slug}`);
}
console.log('  ' + '─'.repeat(60));
console.log('  Files created per issue:');
console.log('    engine/briefs/{slug}.md                              (stub)');
console.log('    engine/output/{slug}-stage1.json                     (from current cards)');
console.log('    engine/prompts-generated/{slug}-stage{2,3,4,5}-browser.txt');
console.log('');
console.log('  Next: paste the browser prompts into Gemini/ChatGPT/DeepSeek/Grok.');
console.log('');
